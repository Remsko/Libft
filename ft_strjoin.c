#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (s1 == NULL && s2 == NULL)
		return (NULL);
	if (!(str = (char*)malloc(sizeof(*str) * (ft_strlen(s1) + \
						ft_strlen(s2) + 1))))
		return (NULL);
	while (i < ft_strlen(s1))
	{
		str[i] = s1[i];
		i++;
	}
	while (j < ft_strlen(s2))
	{
		str[ft_strlen(s1) + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	return (str);
}
